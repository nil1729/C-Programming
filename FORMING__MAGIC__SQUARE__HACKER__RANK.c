int main()
{
    int arr[3][3];
    for(int i=0;i<3;i++)
        for(int j=0;j<3;j++)
            scanf("%d",&arr[i][j]);
    int p[][3][3]={{{4,3,8},{9,5,1},{2,7,6}},{{8,3,4},{1,5,9},{6,7,2}},{{6,7,2},{1,5,9},{8,3,4}},{{2,7,6},{9,5,1},{4,3,8}},{{4,9,2},{3,5,7},{8,1,6}},{{2,9,4},{7,5,3},{6,1,8}},{{8,1,6},{3,5,7},{4,9,2}},{{6,1,8},{7,5,3},{2,9,4}}};
    int count[8]={};
    for(int k=0;k<8;k++)
    {
        for(int i=0;i<3;i++)
        {
            for(int j=0;j<3;j++)
            {
                count[k]+=abs(arr[i][j]-p[k][i][j]);
            }
        }
    }
    int min=100;
    for(int i=0;i<8;i++)
        if(count[i]<min)
            min=count[i];
    printf("%d",min);
    return 0;
}
